package com.homepage.arsenal.config;

import com.homepage.arsenal.domain.Member;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;
import java.util.Collection;
import java.util.List;
import java.util.stream.Collectors;


public class DefaultUser implements UserDetails {

    private final Member member;
    private List<GrantedAuthority> authorities;

    public DefaultUser(Member member) {
        this.member = member;
        authorities = member.getRoles().stream()
                .map(authority -> new SimpleGrantedAuthority(authority.getRoleType().name()))
                .collect(Collectors.toList());
    }

    @Override
    public String getUsername() {
        return member.getEmail();
    }

    @Override
    public String getPassword() {
        return member.getPassword();
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return authorities;
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
